import fs from "node:fs";
import path from "node:path";
import readline from "node:readline";
import { fileURLToPath } from "node:url";

const __filename = fileURLToPath(import.meta.url);
const __dirname = path.dirname(__filename);
const rootDir = path.resolve(__dirname, "..");
const gramDir = path.join(rootDir, "gram_data");
const outputPath = path.join(rootDir, "js", "generated-ngram-data.js");
const SCORE_SCALE = 1000;
const WORD_SOURCE = {
  filename: "english_words.txt",
  limit: 120000,
};

const SOURCES = [
  { n: 1, weight: 0.02, filename: "english_monograms.txt", limit: 26 },
  { n: 2, weight: 0.06, filename: "english_bigrams.txt", limit: 676 },
  { n: 3, weight: 0.12, filename: "english_trigrams.txt", limit: 8000 },
  { n: 4, weight: 0.35, filename: "english_quadgrams.txt", limit: 15000 },
  { n: 5, weight: 0.45, filename: "english_quintgrams.txt", limit: 25000 },
];

function quantizeScore(score) {
  return Math.round(score * SCORE_SCALE);
}

async function readTopEntries(filename, limit) {
  const entries = [];
  let total = 0;
  let nextCount = null;

  const stream = fs.createReadStream(path.join(gramDir, filename), { encoding: "utf8" });
  const lineReader = readline.createInterface({
    input: stream,
    crlfDelay: Infinity,
  });

  for await (const line of lineReader) {
    const trimmed = line.trim();
    if (!trimmed) continue;

    const [gram, countText] = trimmed.split(/\s+/);
    const count = Number.parseInt(countText, 10);
    if (!gram || !Number.isFinite(count)) continue;

    total += count;

    if (entries.length < limit) {
      entries.push([gram.toUpperCase(), count]);
    } else if (nextCount === null) {
      nextCount = count;
    }
  }

  if (entries.length === 0) {
    throw new Error(`No n-gram entries loaded from ${filename}`);
  }

  const lastCount = entries[entries.length - 1][1];
  const floorCount = nextCount ?? Math.max(1, Math.round(lastCount * 0.5));
  const floor = quantizeScore(Math.log10(floorCount / total));
  const scoredEntries = entries.map(([gram, count]) => [
    gram,
    quantizeScore(Math.log10(count / total)),
  ]);

  return {
    entries: scoredEntries,
    floor,
  };
}

async function readTopWords(filename, limit) {
  const entries = [];
  let total = 0;
  let nextCount = null;

  const stream = fs.createReadStream(path.join(gramDir, filename), { encoding: "utf8" });
  const lineReader = readline.createInterface({
    input: stream,
    crlfDelay: Infinity,
  });

  for await (const line of lineReader) {
    const trimmed = line.trim();
    if (!trimmed) continue;

    const [wordText, countText] = trimmed.split(/\s+/);
    const word = wordText?.toUpperCase();
    const count = Number.parseInt(countText, 10);

    if (!word || !/^[A-Z]+$/.test(word) || !Number.isFinite(count)) continue;

    total += count;

    if (entries.length < limit) {
      entries.push([word, count]);
    } else if (nextCount === null) {
      nextCount = count;
    }
  }

  if (entries.length === 0) {
    throw new Error(`No word entries loaded from ${filename}`);
  }

  const lastCount = entries[entries.length - 1][1];
  const floorCount = nextCount ?? Math.max(1, Math.round(lastCount * 0.5));

  return {
    entries: entries.map(([word, count]) => [
      word,
      quantizeScore(Math.log10(count / total)),
    ]),
    floor: quantizeScore(Math.log10(floorCount / total)),
  };
}

async function build() {
  const tables = [];

  for (const source of SOURCES) {
    const { entries, floor } = await readTopEntries(source.filename, source.limit);
    tables.push({
      n: source.n,
      weight: source.weight,
      floor,
      entries,
    });
  }

  const wordData = await readTopWords(WORD_SOURCE.filename, WORD_SOURCE.limit);

  const fileContents = `// Auto-generated by scripts/build-ngram-module.mjs
// Source: gram_data/*.txt

export const GENERATED_NGRAM_SCORE_SCALE = ${SCORE_SCALE};
export const GENERATED_NGRAM_TABLES = ${JSON.stringify(tables, null, 2)};
export const GENERATED_WORD_FLOOR = ${wordData.floor};
export const GENERATED_WORD_SCORES = ${JSON.stringify(wordData.entries, null, 2)};
`;

  fs.writeFileSync(outputPath, fileContents);
  console.log(`Wrote ${outputPath}`);
}

build().catch((error) => {
  console.error(error);
  process.exitCode = 1;
});
